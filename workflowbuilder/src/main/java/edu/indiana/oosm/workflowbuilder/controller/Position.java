package edu.indiana.oosm.workflowbuilder.controller;

public class Position {
	private float top;
	private float left;
	public float getTop() {
		return top;
	}
	public void setTop(float top) {
		this.top = top;
	}
	public float getLeft() {
		return left;
	}
	public void setLeft(float left) {
		this.left = left;
	}
	
}
